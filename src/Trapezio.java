
public class Trapezio {

	/**
	 * Metodo para calcular o Trapezio
	 * @param basemaior
	 * @param basemenor
	 * @param altura
	 * @return result
	 */
	
	double soma(double basemaior, double basemenor, double altura){

		double result = ((basemaior + basemenor)/2) * altura;
		
		return result;
	}
	
}
